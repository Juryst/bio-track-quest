import { Analysis, AiReview, Profile } from '@/types';

export function generateMockAiReview(analysis: Analysis, _profile?: Profile): AiReview {
  const abnormalMarkers = analysis.markers.filter(m => m.status === 'low' || m.status === 'high');
  const borderlineMarkers = analysis.markers.filter(m => m.status === 'borderline');

  if (analysis.type === 'ОАК' && abnormalMarkers.length > 0) {
    return {
      summary: `В анализе ${abnormalMarkers.length} показателя вне нормы. Картина характерна для железодефицитной анемии.`,
      concerns: [
        'Гемоглобин снижен — кровь хуже переносит кислород, возможна слабость и утомляемость',
        'Ферритин критически низкий — запасы железа в организме истощены',
        'Средний объём гемоглобина в эритроците (MCH) снижен — признак микроцитарной анемии',
      ],
      trends: [
        'Гемоглобин снизился по сравнению с предыдущим анализом (112 → 108 г/л)',
        'Ферритин также снижается — запасы железа продолжают истощаться',
      ],
      nextSteps: [
        'Обратиться к терапевту или гематологу',
        'Сдать сывороточное железо и ОЖСС для подтверждения диагноза',
        'Рассмотреть диету, богатую железом (красное мясо, печень, бобовые)',
      ],
      questionsForDoctor: [
        'Нужна ли мне терапия препаратами железа?',
        'Какова вероятная причина анемии?',
        'Стоит ли проверить уровень витамина B12?',
      ],
      disclaimer: 'Это информационная поддержка, а не медицинский диагноз. Обязательно проконсультируйтесь с врачом.',
    };
  }

  if (analysis.type === 'Биохимия') {
    return {
      summary: `В анализе ${abnormalMarkers.length} показателя выше нормы${borderlineMarkers.length > 0 ? ` и ${borderlineMarkers.length} на границе` : ''}.`,
      concerns: [
        'Холестерин ЛПНП повышен — увеличивает риск атеросклероза',
        'Общий холестерин выше нормы — рекомендуется пересмотреть диету',
      ],
      trends: [],
      nextSteps: [
        'Скорректировать питание: снизить жиры, увеличить клетчатку',
        'Повторить анализ через 3 месяца',
        'При необходимости консультация кардиолога',
      ],
      questionsForDoctor: [
        'Нужны ли мне статины?',
        'Какой уровень холестерина является целевым для меня?',
      ],
      disclaimer: 'Это информационная поддержка, а не медицинский диагноз. Обязательно проконсультируйтесь с врачом.',
    };
  }

  return {
    summary: 'Все показатели в пределах нормы. Результаты анализа не вызывают опасений.',
    concerns: [],
    trends: [],
    nextSteps: ['Продолжайте регулярно сдавать анализы для мониторинга здоровья'],
    questionsForDoctor: [],
    disclaimer: 'Это информационная поддержка, а не медицинский диагноз. Обязательно проконсультируйтесь с врачом.',
  };
}
